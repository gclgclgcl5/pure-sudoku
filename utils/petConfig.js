function getPlayablePetIdsSortedForGallery() {
  return Object.keys(PETS).sort((a, b) => {
    const oa = PETS[a].galleryOrder;
    const ob = PETS[b].galleryOrder;
    return (typeof oa === 'number' ? oa : 999) - (typeof ob === 'number' ? ob : 999);
  });
}

function isPlayablePetId(id) {
  return typeof id === 'string' && Object.prototype.hasOwnProperty.call(PETS, id);
}

function getPetById(id) {
  return PETS[id] || null;
}

function getLine(petId, lineKey) {
  const pet = getPetById(petId);
  if (!pet) return '';
  const raw = pet.lines && pet.lines[lineKey];
  return trimBubble(pickRandomLine(raw));
}

function getWinLine(petId) {
  const pet = getPetById(petId);
  if (!pet) return '';
  return trimBubble(pickRandomLine(pet.winLine));
}

function getTapGalleryLine(petId) {
  const pet = getPetById(petId);
  if (!pet) return '';
  return trimBubble(pickRandomLine(pet.tapGallery));
}

function getGalleryPetsOrdered() {
  return getPlayablePetIdsSortedForGallery()
    .map((id) => PETS[id])
    .filter(Boolean);
}

